package ArrayWork1;

public class Arrayttraversing {

	//Creating a method which is non-static like behaviour.
	public void printarrayElements(int []arr)
	{
		System.out.println("*************************************");
		for(int i=0;i<arr.length;i++)
		{
			System.out.print(arr[i]+" ");
		}
		System.out.println();
		System.out.println("*************************************");
	}
	
}
