package ArrayWork1;

public class ArrayresTraversing {
	public void printarray(int []arr)
	{
		System.out.println("*****************************");
		for(int i=0;i<arr.length;i--)
		{
			System.out.println(arr[i]);
		}
		System.out.println();
		System.out.println("*****************************");
	}

}
